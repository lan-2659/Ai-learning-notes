# 最近邻插值
"""
原理：
    直接取目标像素位置最近的原始像素值  
    数学公式：  
        dst(x,y) = src(round(x_ratio), round(y_ratio))  
        （其中 x_ratio = x * src_width/dst_width, y_ratio同理）

特点：
    ⚡️ 计算速度最快  
    🟢 保持原始像素值（不引入新颜色）  
    🔴 产生锯齿状边缘（马赛克效应）

适用场景：
    实时性要求高的场景（如视频流处理）  
    需要保留像素原始值的科学计算
"""


# 双线性插值
"""
原理：
    对目标点周围4个最近像素进行两次线性插值  
    步骤：  
        1. 水平方向两次线性插值  
        2. 垂直方向一次线性插值  
    数学公式（简化版）：  
        f(x,y) = (1−α)(1−β)f(x0,y0) + α(1−β)f(x1,y0)  
                + (1−α)βf(x0,y1) + αβf(x1,y1)  
        （其中 α, β 为归一化后的距离权重）

特点：
    ⚡️ 速度适中  
    🟢 平滑边缘，减少锯齿  
    🔴 轻微模糊细节

适用场景：
    大多数常规缩放需求（OpenCV默认方法）  
    需要平衡速度与质量的场景
"""


# 双三次插值
"""
原理：
    使用周围16个像素，通过三次多项式计算权重  
    数学基础：  
        采用类似 sin(x)/x 的核函数（如Mitchell-Netravali核）  
        计算每个像素的加权贡献  
    公式复杂度：  
        f(x,y) = Σ_{i=-1}^2 Σ_{j=-1}^2 f(x_i,y_j) * W(Δx_i) * W(Δy_j)  
        （W为三次权重函数，Δ为相对距离）

特点：
    🐢 计算最慢  
    🟢 保留更多细节  
    🔴 可能产生过冲（overshooting）伪影

适用场景：
    高质量图像放大（如照片打印）  
    需要保留高频信息的医学影像处理
"""


# 区域重采样
"""
原理：
    当缩小图像时，计算目标像素对应源图像区域的像素平均值  
    数学本质：  
        像素面积积分 → 离散形式的箱式滤波  
    特殊处理：  
        放大时退化为最近邻插值（因此不推荐用于放大）

特点：
    ⚡️ 缩小图像时速度较快  
    🟢 消除摩尔纹和锯齿  
    🔴 放大时效果差

适用场景：
    图像缩小（OpenCV推荐方法）  
    需要避免混叠（aliasing）的信号处理
"""